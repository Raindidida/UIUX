// 建立拼音->成语 映射（按首字拼音索引）
export const IDIOM_MAP = new Map<string, Idiom[]>();
IDIOMS.forEach(idiom => {
  const key = idiom.first;
  if (!IDIOM_MAP.has(key)) IDIOM_MAP.set(key, []);
  IDIOM_MAP.get(key)!.push(idiom);
});

// 按文字查找成语
export function findIdiom(text: string): Idiom | undefined {
  return IDIOMS.find(i => i.text === text);
}

// 检查接龙是否有效：输入成语的首字拼音 === 上一成语末字拼音
export function isValidChain(input: Idiom, previous: Idiom): boolean {
  return input.first === previous.last;
}

// 获取 AI 可以接龙的成语列表
export function getChainCandidates(previous: Idiom): Idiom[] {
  return IDIOM_MAP.get(previous.last) || [];
}

// 随机起始成语
export function getRandomStartIdiom(): Idiom {
  return IDIOMS[Math.floor(Math.random() * IDIOMS.length)];
}
